    private void btnAbrirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirArchivoActionPerformed
        if(seleccionado.showDialog (this, "Abrir archivo")== JFileChooser.APPROVE_OPTION)
        {
            archivo = seleccionado.getSelectedFile();
            if(archivo.canRead())
            {
                if(archivo.getName().endsWith("txt"))
                {
                    String contenido =  gestion.AbrirATexto(archivo);
                    txtAreaTexto.setText(contenido);//se recupera informacion del contenido del txt en el area de texto (txtAreaTexto asi se llama la variable del framde de area texto)
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Elige otro archivo");
                }
            }
        }
    }//GEN-LAST:event_btnAbrirArchivoActionPerformed

    private void btnGuardarTextoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTextoActionPerformed
        //cuardar textoj
        if(seleccionado.showDialog (null,"Guardar laberinto") == JFileChooser.APPROVE_OPTION) 
        {
            archivo = seleccionado.getSelectedFile();
            if(archivo.getName().endsWith("txt"))
            {
                String contenido = txtAreaTexto.getText();//se crea variable contenido en donde se obtendra laberinto
                String respuesta = gestion.GuardarATexto(archivo, contenido);// se almacena el nuevo contenido del laberinto modificado
                if(respuesta != null)
                {
                    JOptionPane.showMessageDialog(null,"El laberinto se guardo con exito");
                    
                }
                else 
                {
                     JOptionPane.showMessageDialog(null,"No se guardo texto");
                }
            }
        }
    }//GEN-LAST:event_btnGuardarTextoActionPerformed

